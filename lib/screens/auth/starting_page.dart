import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_screenutil/flutter_screenutil.dart';
import 'package:lost_animal/blocs/start_bloc/start_bloc.dart';
import 'package:lost_animal/blocs/start_bloc/start_event.dart';
import 'package:lost_animal/blocs/start_bloc/start_state.dart';
import 'package:lost_animal/screens/auth/log_in_page.dart';
import 'package:lost_animal/screens/auth/sing_up_page.dart';
import 'package:lost_animal/widgets/tabsbar.dart';

import '../../constants.dart';
import '../../local_storage.dart';

class StartScreen extends StatefulWidget {
  const StartScreen({Key key}) : super(key: key);

  @override
  _StartScreenState createState() => _StartScreenState();
}

class _StartScreenState extends State<StartScreen> {

  LocalStorage storage;
  bool register;

  @override
  void initState() {
    BlocProvider.of<StartBloc>(context).add(RegisterInfoLoad());
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    ScreenUtil.init(context,
        designSize: DesignConfig.size, allowFontScaling: true);
    return BlocListener<StartBloc, StartState>(
        listener: (BuildContext context, StartState state) {
          if (state is RegisterInfoLoaded) {
            this.register = state.registered;
          }
          if (register == null) {
            return Center(child: loadingCircle());
          } else if (register == true) {
            Navigator.push(
              context,
              MaterialPageRoute(
                builder: (context) => TabsBarScreen(),
              ),
            );
          } else {
            return authFirstPage();
          }
        },
        child: authFirstPage()
    );
  }

  Widget authLoadPage() {
    return Scaffold(
      appBar: myAppBar(),
      body: SafeArea(
        child: Container(
            padding: const EdgeInsets.symmetric(vertical: 250),
            child: ListView(
              children: [
                Column(
                    crossAxisAlignment: CrossAxisAlignment.center,
                    mainAxisAlignment: MainAxisAlignment.center,
                    children: [
                        Center(child: loadingCircle())
                    ]
                )
              ],
            )
        ),
      ),
    );
  }

  Widget authFirstPage() {
    return Scaffold(
      appBar: myAppBar(),
      body: SafeArea(
        child: Container(
            padding: const EdgeInsets.symmetric(vertical: 250),
            child: ListView(
              children: [
                Column(
                    crossAxisAlignment: CrossAxisAlignment.center,
                    mainAxisAlignment: MainAxisAlignment.center,
                    children: [
                      logInButton(),
                      SizedBox(
                        height: 10.h,
                      ),
                      singUpButton()
                    ]
                )
              ],
            )
        ),
      ),
    );
  }

  Widget logInButton() {
    return Container(
      width: 260.0.w,
      height: 65.0.h,
      child: FloatingActionButton(
        heroTag: "login_button",
        backgroundColor: Color(0xFF63B4FF),
        shape: RoundedRectangleBorder(
            borderRadius: BorderRadius.all(Radius.circular(5))),
        child: Text(
          'Log in',
          style: TextStyle(
            color: Colors.white,
            fontWeight: FontWeight.w700,
            fontSize: 24.sp,
          ),
        ),
        onPressed: () {
          Navigator.push(
              context,
              MaterialPageRoute(builder: (context) => LogInScreen())
          );
        },
      ),
    );
  }

  Widget singUpButton() {
    return Container(
      width: 260.0.w,
      height: 65.0.h,
      child: FloatingActionButton(
        heroTag: "signup_button",
        backgroundColor: Color(0xFF000033),
        shape: RoundedRectangleBorder(
            borderRadius: BorderRadius.all(Radius.circular(5))),
        child: Text(
          'Sign up',
          style: TextStyle(
            color: Colors.white,
            fontWeight: FontWeight.w700,
            fontSize: 24.sp,
          ),
        ),
        onPressed: () {
          Navigator.push(
              context,
              MaterialPageRoute(builder: (context) => SingUpScreen())
          );
        },
      ),
    );
  }

  Widget myAppBar() {
    return
      AppBar(
        title:
        Padding(
          padding: EdgeInsets.only(top: 10.h),
          child: Text(
            'PetFinder',
            style: TextStyle(
              color: Color(0xFF000033),
              fontWeight: FontWeight.bold,
              fontSize: 25.sp,
            ),
          ),
        ),
        centerTitle: false,
        backgroundColor: Color(0xFF63B4FF),
        elevation: 0,
      );
  }

  Widget loadingCircle() {
    Widget indicator = CupertinoActivityIndicator();
    return indicator;
  }
}

